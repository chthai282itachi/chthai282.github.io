public class StandardizedPatientListActivity extends AbstractProxyListActivity<StandardizedPatientProxy> implements IsScaffoldMobileActivity {

    private final ApplicationRequestFactory requests;

    public StandardizedPatientListActivity(ApplicationRequestFactory requests, ProxyListView<ch.unibas.medizin.osce.client.managed.request.StandardizedPatientProxy> view, PlaceController placeController) {
        super(placeController, view, StandardizedPatientProxy.class);
        this.requests = requests;
    }

    public Place getBackButtonPlace() {
        return ScaffoldMobileApp.ROOT_PLACE;
    }

    public String getBackButtonText() {
        return "Entities";
    }

    public Place getEditButtonPlace() {
        return null;
    }

    public String getTitleText() {
        return "StandardizedPatients";
    }

    public boolean hasEditButton() {
        return false;
    }

    protected Request<java.util.List<ch.unibas.medizin.osce.client.managed.request.StandardizedPatientProxy>> createRangeRequest(Range range) {
        return requests.standardizedPatientRequest().findStandardizedPatientEntries(range.getStart(), range.getLength());
    }

    protected void fireCountRequest(Receiver<Long> callback) {
        requests.standardizedPatientRequest().countStandardizedPatients().fire(callback);
    }
}
